#include <stdint.h>
int main(int argc, char* argv[] ){
	uint8_t I;
	uint8_t O;
	I &= 0b11111;
    
	if( 0 <= I && I < 2 ) { O = 4; }
	else if( 2 <= I && I < 4 ) { O = 5; }
	else if( 4 <= I && I < 8 ) { O = 6; }
	else if( 8 <= I && I < 13 ) { O = 7; }
	else if( 13 <= I && I < 18 ) { O = 8; }
	else if( 18 <= I && I < 23 ) { O = 9; }
	else if( 23 <= I && I < 27 ) { O = 10; }
	else if( 27 <= I && I < 29 ) { O = 11; }
	else if( 29 <= I && I < 30 ) { O = 12; }
	else if( 30 <= I && I < 32 ) { O = 13; }
	assert(0);
}
